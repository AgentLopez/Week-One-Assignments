first = int(input("First Number >"))
dotoit = input("Would you like + - * or / >")
last = int(input("Second Number >"))

if (dotoit == "+"):
    print(first + last)

elif (dotoit == "-"):
    print(first - last)

elif (dotoit == "*"):
    print(first * last)

elif (dotoit == "/"):
    print(first / last)